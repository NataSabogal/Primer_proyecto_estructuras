    private void btnAlquilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlquilarActionPerformed
        if (!txtNombre.getText().isEmpty() && !txtPregunta.getText().isEmpty() && !txtRespuesta.getText().isEmpty() && !txtValor.getText().isEmpty() && !txtfecha.getText().isEmpty()) {
            String nombre = txtNombre.getText();
            String pregunta = txtPregunta.getText();
            String respuesta = txtRespuesta.getText();
            double alquiler = Double.parseDouble(txtValor.getText());
            String fecha = txtfecha.getText();
            boolean aux = controller.alquilarContenedor(fila, columna, nombre, fecha, alquiler, pregunta, respuesta);
            if (aux) {
                JOptionPane.showMessageDialog(null, "Se alquilo correctamente");
                VentanaPrincipal prin = new VentanaPrincipal();
                prin.setLocationRelativeTo(null);
                prin.setVisible(true);
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Asegurese de digitar todos los campos para ser alquilado");
        }


    }//GEN-LAST:event_btnAlquilarActionPerformed

